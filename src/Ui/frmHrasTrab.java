    private void cargarEmpleados() {

        gestor.conectarBD();
        rs = gestor.seleccionar("SELECT nombre FROM tblusuarios s inner join tblhorarios h ON s.idUsuario=h.idUsuario where s.idTipoUsuario=2 AND (h.idJornada >0) GROUP BY s.nombre ");

        if (rs != null) {
            try {
                while (rs.next()) {
                    cboEmpleado.addItem(rs.getString(1).toString());
                }
            } catch (SQLException ex) {
                gestor.desconectarBD();
                Logger.getLogger(frmAgregarHorarios.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        gestor.desconectarBD();
    }

    public void borrartbl() {
        int a = tblHT1.getRowCount();
        for (int i = a; i >= 0; i--) {
            tblHT1.remove(i);
        }
    }

