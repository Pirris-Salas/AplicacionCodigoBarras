    private void cboEmpleadoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboEmpleadoItemStateChanged

        String selected;

        selected = cboEmpleado.getSelectedItem().toString();

        gestor.conectarBD();

        rs = gestor.likeEmpleado(selected);

        try {
            if (rs.next()) { //Si lo encuentra

                lblCedula.setText("" + rs.getInt("idusuario"));
                lblEmpleado1.setText(rs.getString("nombre"));

            } else {

                JOptionPane.showMessageDialog(rootPane, "El empleado no se ha encontrado.");

            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        gestor.desconectarBD();

    }//GEN-LAST:event_cboEmpleadoItemStateChanged

    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed

        gestor = new GestorHorasTRab();
        Date uno = dcFinal.getDate();
        Date dos = dcFinal1.getDate();

        SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String fecha1 = sdf.format(uno);
        String fecha2 = sdf.format(dos);
        int id = Integer.parseInt(lblCedula.getText());

        gestor.conectarBD();

        rs = gestor.likeHorario(id, fecha1, fecha2);

        if (rs != null) {

            tblHT.setModel(gestor.cargarEnTabla(rs));

        }

        gestor.desconectarBD();

        DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "Extras"
            }
        );
        if (tblHT.getRowCount() > 0) {//normales
            for (int i = 0; i < tblHT.getRowCount(); i++) {
                String total = tblHT.getValueAt(i, 2).toString();
                String min = total.substring(6, 8);
                int extras = Integer.parseInt(total.substring(3, 5)) - 48;
                if (Integer.parseInt(total.substring(3, 5)) < 48) {
                    model.addRow(new String[]{total.substring(0, 2)});
                } else {
                    model.addRow(new String[]{Integer.toString(extras) + ":" + min});
                }
            }
            tblHT1.setModel(model);
        }
    }//GEN-LAST:event_btnProcesarActionPerformed


