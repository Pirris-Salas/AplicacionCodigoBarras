    private void btnSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalirActionPerformed

        String codigo = "";
        String clave = "";
        int resp = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de cerrar el sistema Invenio Training Center?",
                "ADVERTENCIA¡¡¡¡¡¡", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
            clave = JOptionPane.showInputDialog("Digite el código de seguridad");
            gestorDatos.conectarBD();

            ResultSet rs = gestorDatos.seleccionar("select codigosalida from tblsalida");
            try {
                if (rs.next()) {
                    codigo = rs.getString(1);
                }
            } catch (Exception e) {
            }
            gestorDatos.desconectarBD();
            if (clave.equals(codigo)) {

                System.exit(1);
            } else {

                JOptionPane.showMessageDialog(rootPane, "Código de seguridad incorrecto, por favor verifiquelo y vuelva a intentar",
                        "Código incorrecto", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSalirActionPerformed

    public static String fechaActual() {
        Date fecha = new Date();
        SimpleDateFormat formateador = new SimpleDateFormat("yyyy-MM-dd");
        return formateador.format(fecha);
    }

