    private void txtCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodigoFocusLost
        if (!txtCodigo.getText().equals("")) {
            //      ---------Busca-------------------------
            int codigo=0;
            try{
                codigo = Integer.parseInt(txtCodigo.getText());
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane, "En la identificacion solo se aceptan numeros");
                codigo=0;
            }
            
            info = gestorDatos.buscarDato(codigo);
            vali = gestorDatos.validacion(codigo);
            
            if (info != null) {
                txtNombre.setText(info.getNombre() + " " + info.getApellidos());
                //      ----------------Guarda el Codigo Fecha y Hora-----------------------
                dato = new clsDatos();
                dato.setCodigo(Integer.parseInt(txtCodigo.getText()));
                dato.setFecha(lblFecha.getText() + " " + lblReloj.getText());

//            JOptionPane.showMessageDialog(rootPane, dato.getFecha());
                if (gestorDatos.guardarDato(dato) != false) {

                    Date uno = new Date();
//   este no   uno = dcInicio.getCalendar().getTime();
//    no  dos = dcFinal.getCalendar().getTime();

                    SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd-MM-yyyy");
                    String fecha1 = sdf.format(uno);

                    if (vali.getHora() != null) {

                        JOptionPane.showMessageDialog(rootPane, info.getNombre() + " " + info.getApellidos() + " " + " su salida ha sido registrada exitosamente a las " + lblReloj.getText() + " el dia " + fecha1);
//ja.setText(vali.getHora());
                    } else {

                        JOptionPane.showMessageDialog(rootPane, info.getNombre() + " " + info.getApellidos() + " " + " su entrada ha sido registrada exitosamente a las " + lblReloj.getText() + " el dia " + fecha1);
//        ja.setText(vali.getHora());
                    }
                    limpiarCampos();
                    txtCodigo.requestFocus();

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Error al intentar guardar ");
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Empleado no encontrado, por favor verifique su identificacion",
                        "Buscar empleado", JOptionPane.ERROR_MESSAGE);
                limpiarCampos();
            }
            txtCodigo.requestFocus();
        }
    }//GEN-LAST:event_txtCodigoFocusLost

